const fs = require('fs');
const path = require('path');

const root = process.cwd();
const outDir = path.join(root, 'www');

const FILES = [
  'index.html',
  'styles.css',
  'manifest.json',
  'sw.js'
];

const DIRS = [
  'assets',
  'js'
];

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function cleanOutDir() {
  fs.rmSync(outDir, { recursive: true, force: true });
  ensureDir(outDir);
}

function copyFile(relPath) {
  const src = path.join(root, relPath);
  if (!fs.existsSync(src)) {
    console.warn(`[native:web] Skipping missing file: ${relPath}`);
    return;
  }
  const dest = path.join(outDir, relPath);
  ensureDir(path.dirname(dest));
  fs.copyFileSync(src, dest);
  console.log(`[native:web] File: ${relPath}`);
}

function copyDir(relPath) {
  const src = path.join(root, relPath);
  if (!fs.existsSync(src)) {
    console.warn(`[native:web] Skipping missing directory: ${relPath}`);
    return;
  }
  const dest = path.join(outDir, relPath);
  fs.cpSync(src, dest, { recursive: true });
  console.log(`[native:web] Directory: ${relPath}`);
}

function writeNativeReadme() {
  const readmePath = path.join(outDir, 'README_NATIVE_WEB.txt');
  const lines = [
    'This folder is generated by scripts/prepare-native-web.js.',
    'Do not edit files here directly.',
    '',
    `Generated at: ${new Date().toISOString()}`
  ];
  fs.writeFileSync(readmePath, lines.join('\n'));
}

function main() {
  console.log('[native:web] Preparing native web bundle...');
  cleanOutDir();
  FILES.forEach(copyFile);
  DIRS.forEach(copyDir);
  writeNativeReadme();
  console.log('[native:web] Done. Output: ./www');
}

main();

